class Hiker:
    def __init__(self, name, altitude=0, thirst=false):
        self.name = name
        self.altitude = altitude
        self.thirst = thirst

    def climb(amount):
        self.altitude += amount
        self.thirst = true
    
    def descend(amount):
        self.altitude -= amount
        self.thirst = true

    def drinkbreak():
        self.thirst = false